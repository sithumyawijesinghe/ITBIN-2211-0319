    private void btaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btaddActionPerformed
        // TODO add your handling code here:
        try{
        String roomnumber,roomtype,bed,price;
        
        roomnumber=txtroomnumber.getText();
        roomtype=(String) cbroomtype.getSelectedItem();
        bed=(String) cbbed.getSelectedItem();
        price=txtprice.getText();
        
        controller.addRoom(roomnumber, roomtype, bed, price);
        JOptionPane.showMessageDialog(this, "Record saved");
        table_update();
        
        txtroomnumber.setText("");
        cbroomtype.setSelectedItem("");
        cbbed.setSelectedItem("");
        txtprice.setText("");
        txtroomnumber.requestFocus();
       
        
        }catch (SQLException ex){
            JOptionPane.showMessageDialog(this,"error" + ex.getMessage());
        }
    }//GEN-LAST:event_btaddActionPerformed

    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        int selectedIndex = jTable2.getSelectedRow();
        
        txtroomnumber.setText(model.getValueAt(selectedIndex, 0).toString());
        cbroomtype.setSelectedItem(model.getValueAt(selectedIndex, 1).toString());
        cbbed.setSelectedItem(model.getValueAt(selectedIndex, 2).toString());
        txtprice.setText(model.getValueAt(selectedIndex, 3).toString());
        
        
        
    }//GEN-LAST:event_jTable2MouseClicked

    private void bteditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bteditActionPerformed
        // TODO add your handling code here:
         try{
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        int selectedIndex = jTable2.getSelectedRow();
        
        String originalroomnumber = model.getValueAt(selectedIndex, 0).toString();
             
        String roomnumber,roomtype,bed,price;
        
        roomnumber=txtroomnumber.getText();
        roomtype=(String) cbroomtype.getSelectedItem();
        bed=(String) cbbed.getSelectedItem();
        price=txtprice.getText();
        
        controller.updateRoom(roomnumber, roomtype, bed, price, originalroomnumber);
        JOptionPane.showMessageDialog(this, "Record updated");
        table_update();
        
        txtroomnumber.setText("");
        cbroomtype.setSelectedItem("");
        cbbed.setSelectedItem("");
        txtprice.setText("");
        txtroomnumber.requestFocus();
       
        
        }catch (SQLException ex){
            JOptionPane.showMessageDialog(this,"error" + ex.getMessage());
        }
        
    }//GEN-LAST:event_bteditActionPerformed

    private void btdeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btdeleteActionPerformed
        // TODO add your handling code here:
         try{
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        int selectedIndex = jTable2.getSelectedRow();
        
        String originalroomnumber = model.getValueAt(selectedIndex, 0).toString();
            
        
        controller.deleteRoom(originalroomnumber);
        JOptionPane.showMessageDialog(this, "Record deleted");
        table_update();
        
        txtroomnumber.setText("");
        cbroomtype.setSelectedItem("");
        cbbed.setSelectedItem("");
        txtprice.setText("");
        txtroomnumber.requestFocus();
       
        
        }catch (SQLException ex){
            JOptionPane.showMessageDialog(this,"error" + ex.getMessage());
        }
    }//GEN-LAST:event_btdeleteActionPerformed

