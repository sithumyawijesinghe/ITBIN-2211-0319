    public void table_update(){
       DefaultTableModel dft = (DefaultTableModel) jTable2.getModel();
        dft.setRowCount(0);
        Vector<Vector<Object>> data = controller.getAllRooms();
        for (Vector<Object> row : data) {
            dft.addRow(row);
        }
    }
    
    
    
    
